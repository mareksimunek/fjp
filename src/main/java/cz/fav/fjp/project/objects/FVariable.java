package cz.fav.fjp.project.objects;

public class FVariable {
	
	private String type;
	private String name;
	
	public FVariable() {
		super();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
